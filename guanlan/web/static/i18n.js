let _lang = "zh";
window.currentLang = () => _lang;
window.setLang = (lang) => { _lang = (lang === "en") ? "en" : "zh"; };

// 取词：缺 key 回退到「另一语言」、再回退到 key 本身（永不抛、永不显示 undefined，决策P4.7-4）。
window.t = (key, ...args) => {
  const other = (_lang === "en") ? window.I18N.zh : window.I18N.en; // 当前语言缺 key 时回退的另一侧
  const dict = window.I18N[_lang] || window.I18N.zh;
  const s = (key in dict) ? dict[key] : ((key in other) ? other[key] : key);
  return s.replace(/\{(\d+)\}/g, (_, i) => (args[i] !== undefined ? args[i] : ""));
};
